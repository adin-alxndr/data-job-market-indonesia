"""**Helper: Parse One Page of Job Cards**"""

def parse_jobs(soup):
    jobs = []
    cards = soup.find_all('article', attrs={'data-card-type': 'JobCard'})

    for card in cards:
        title_tag    = card.find('a',    attrs={'data-automation': 'jobTitle'})
        company_tag  = card.find('a',    attrs={'data-automation': 'jobCompany'})
        location_tag = card.find('span', attrs={'data-automation': 'jobCardLocation'})
        salary_tag   = card.find('span', attrs={'data-automation': 'jobSalary'})
        jobtype_tag  = card.find('span', attrs={'data-automation': 'jobWorkType'})
        date_tag     = card.find('span', attrs={'data-automation': 'jobListingDate'})
        desc_tag     = card.find('div',  attrs={'data-automation': 'jobShortDescription'})
        link_tag     = card.find('a',    attrs={'data-automation': 'jobTitle'})

        job_url = ('https://id.jobstreet.com' + link_tag['href']
                   if link_tag and link_tag.get('href') else None)

        jobs.append({
            'title':        title_tag.get_text(strip=True)    if title_tag    else None,
            'company':      company_tag.get_text(strip=True)  if company_tag  else None,
            'location':     location_tag.get_text(strip=True) if location_tag else None,
            'salary':       salary_tag.get_text(strip=True)   if salary_tag   else None,
            'job_type':     jobtype_tag.get_text(strip=True)  if jobtype_tag  else None,
            'date_listed':  date_tag.get_text(strip=True)     if date_tag     else None,
            'description':  desc_tag.get_text(strip=True)     if desc_tag     else None,
            'url':          job_url
        })
    return jobs

"""**Helper: Fetch Full Description from Job Detail Page**"""

def fetch_full_description(driver, url, timeout=8):
    """
    Visit individual job page and extract full job description text.
    Returns empty string if failed or timed out.
    """
    if not url:
        return ''
    try:
        driver.get(url)
        WebDriverWait(driver, timeout).until(
            EC.presence_of_element_located(
                (By.CSS_SELECTOR,
                 "[data-automation='jobAdDetails'], "
                 "[data-automation='job-detail-page'], "
                 ".job-detail, #job-description")
            )
        )
        time.sleep(1)
        soup = BeautifulSoup(driver.page_source, 'lxml')

        # Try multiple selectors Jobstreet uses for full description
        for selector in [
            {'data-automation': 'jobAdDetails'},
            {'data-automation': 'job-detail-page'},
            {'class': 'job-detail'},
        ]:
            tag = soup.find(attrs=selector)
            if tag:
                return tag.get_text(separator=' ', strip=True)

        # Fallback: get all text from main content area
        main = soup.find('main') or soup.find('article')
        return main.get_text(separator=' ', strip=True) if main else ''

    except Exception:
        return ''  # silently skip if page fails

"""**Helper: Scrape Full Description from Job Detail Page**"""

def scrape_job_detail(driver, url, timeout=8):
    """
    Visit individual job page and extract full job description.
    Returns full text or None if failed.
    """
    if not url:
        return None
    try:
        driver.get(url)
        WebDriverWait(driver, timeout).until(
            EC.presence_of_element_located(
                (By.CSS_SELECTOR, "[data-automation='jobAdDetails']")
            )
        )
        time.sleep(1)
        soup = BeautifulSoup(driver.page_source, 'lxml')
        # Primary selector for full job description
        detail = soup.find(attrs={'data-automation': 'jobAdDetails'})
        if detail:
            return detail.get_text(separator=' ', strip=True)
        # Fallback selectors
        for sel in ['job-detail-content', 'job-description', 'jobDescription']:
            el = soup.find(class_=sel) or soup.find(id=sel)
            if el:
                return el.get_text(separator=' ', strip=True)
    except Exception:
        pass
    return None

"""**Main Scraper**"""

def scrape_jobstreet(keywords, max_pages=5, scrape_detail=True, detail_limit=50):
    """
    Scrape Jobstreet Indonesia for multiple job keywords.

    Parameters
    ----------
    keywords : list of str
        e.g. ['data scientist', 'data analyst', 'data engineer', 'machine learning']
    max_pages : int
        Maximum pages per keyword (1 page ≈ 30 jobs)
    scrape_detail : bool
        If True, visit each job page to get full description (slower but richer)
    detail_limit : int
        Max number of detail pages to scrape per keyword (to save time)

    Returns
    -------
    pd.DataFrame
    """
    all_jobs = []
    driver = get_driver()

    for keyword in keywords:
        print(f"\n🔍 Scraping: '{keyword}'")
        keyword_slug = keyword.lower().replace(' ', '-')
        detail_count = 0

        for page in range(1, max_pages + 1):
            url = (f"https://id.jobstreet.com/{keyword_slug}-jobs"
                   f"?page={page}&sortmode=ListedDate")
            print(f"   Page {page}: {url}")

            try:
                driver.get(url)
                WebDriverWait(driver, 10).until(
                    EC.presence_of_element_located(
                        (By.CSS_SELECTOR, "article[data-card-type='JobCard']")
                    )
                )
                time.sleep(2)

                soup  = BeautifulSoup(driver.page_source, 'lxml')
                jobs  = parse_jobs(soup)

                if not jobs:
                    print(f"   ⚠️  No jobs found on page {page}, stopping.")
                    break

                # Scrape full description for each job
                if scrape_detail:
                    for job in jobs:
                        if detail_count < detail_limit and job.get('url'):
                            full_desc = scrape_job_detail(driver, job['url'])
                            if full_desc:
                                # Combine snippet + full desc for max coverage
                                job['description'] = (
                                    (job.get('description') or '') + ' ' + full_desc
                                ).strip()
                            detail_count += 1

                for job in jobs:
                    job['search_keyword'] = keyword

                all_jobs.extend(jobs)
                print(f"   ✅ {len(jobs)} jobs collected "
                      f"(detail scraped: {min(detail_count, len(jobs))})")

            except Exception as e:
                print(f"   ❌ Error on page {page}: {e}")
                break

    driver.quit()
    df = pd.DataFrame(all_jobs)
    print(f"\n✅ Total jobs scraped: {len(df)}")
    return df

