# Standardize location — extract city only
def extract_city(loc):
    if pd.isna(loc):
        return 'Unknown'
    loc = str(loc)
    # Take part before comma if exists
    city = loc.split(',')[0].strip()
    # Normalize common city name variations
    city = re.sub(r'(?i)^(south|selatan)\s+jakarta|jakarta\s+(south|selatan)', 'South Jakarta', city)
    city = re.sub(r'(?i)^(north|utara)\s+jakarta|jakarta\s+(north|utara)',     'North Jakarta', city)
    city = re.sub(r'(?i)^(west|barat)\s+jakarta|jakarta\s+(west|barat)',       'West Jakarta',  city)
    city = re.sub(r'(?i)^(east|timur)\s+jakarta|jakarta\s+(east|timur)',       'East Jakarta',  city)
    city = re.sub(r'(?i)^(central|pusat)\s+jakarta|jakarta\s+(central|pusat)', 'Central Jakarta', city)
    city = re.sub(r'(?i)jakarta.*', 'Jakarta', city)
    city = re.sub(r'(?i)bandung.*', 'Bandung', city)
    city = re.sub(r'(?i)surabaya.*', 'Surabaya', city)
    city = re.sub(r'(?i)yogyakarta.*', 'Yogyakarta', city)
    city = re.sub(r'(?i)tangerang.*', 'Tangerang', city)
    city = re.sub(r'(?i)bekasi.*', 'Bekasi', city)
    city = re.sub(r'(?i)depok.*', 'Depok', city)
    city = re.sub(r'(?i)bali.*', 'Bali', city)
    city = re.sub(r'(?i)semarang.*', 'Semarang', city)
    city = re.sub(r'(?i)medan.*', 'Medan', city)
    # Filter out "Indonesia" as city — it's a country, not a city
    if city.strip().lower() in ('indonesia', 'republic of indonesia', ''):
        return 'Unknown'
    return city

df['city'] = df['location'].apply(extract_city)

# Standardize job title into role category
def categorize_role(title):
    if pd.isna(title):
        return 'Other'
    t = title.lower()
    # Data Scientist
    if any(k in t for k in ['data scientist', 'data science', 'scientist data']):
        return 'Data Scientist'
    # ML / AI Engineer
    elif any(k in t for k in ['machine learning', 'ml engineer', 'ai engineer',
                               'deep learning', 'mlops', 'computer vision',
                               'nlp engineer', 'artificial intelligence']):
        return 'ML/AI Engineer'
    # Data Engineer
    elif any(k in t for k in ['data engineer', 'data pipeline', 'etl developer',
                               'data platform', 'data infrastructure',
                               'data warehouse', 'data architect']):
        return 'Data Engineer'
    # Data Analyst — broad matching
    elif any(k in t for k in ['data analyst', 'data analysis', 'analyst data',
                               'analytics engineer', 'reporting analyst',
                               'business data', 'insight analyst']):
        return 'Data Analyst'
    # Business Analyst / BI
    elif any(k in t for k in ['business analyst', 'business intelligence',
                               'bi analyst', 'bi developer', 'bi engineer',
                               'bi specialist', 'power bi', 'tableau developer',
                               'product analyst', 'system analyst',
                               'financial analyst', 'marketing analyst']):
        return 'Business Analyst/BI'
    # Data Manager / Lead
    elif any(k in t for k in ['data manager', 'head of data', 'chief data',
                               'data lead', 'director of data',
                               'vp of data', 'data governance']):
        return 'Data Manager/Lead'
    else:
        return 'Other Data Role'

df['role_category'] = df['title'].apply(categorize_role)

