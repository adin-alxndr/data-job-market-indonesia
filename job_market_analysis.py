def infer_job_type(row):
    if pd.notna(row['job_type']) and row['job_type'] not in ('', 'Not specified'):
        return row['job_type']
    text = str(row.get('title', '')) + ' ' + str(row.get('description', ''))
    text = text.lower()
    if any(k in text for k in ['full time', 'full-time', 'permanent']):
        return 'Full Time'
    elif any(k in text for k in ['part time', 'part-time']):
        return 'Part Time'
    elif any(k in text for k in ['contract', 'freelance', 'project based']):
        return 'Contract'
    elif any(k in text for k in ['intern', 'magang', 'internship']):
        return 'Internship'
    else:
        return 'Not specified'

